class User {
  final String name;
  final String surname;
  final String birthDate;
  final String city;
  final String profession;
  final String email;
  final String password;

  User({
    required this.name,
    required this.surname,
    required this.birthDate,
    required this.city,
    required this.profession,
    required this.email,
    required this.password,
  });
}
